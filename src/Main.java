public class Main {
    public static void main(String[] args) {

        //Арефметические знаки +, -, *, /.

        int Box1 = 5;
        int Box2 = 10;

        int Box3 =584;
        int Box4 =321;

        int Box5 =14;
        int Box6 =18;

        int Box7 =735;
        int Box8 =32;

        //Сложение
        System.out.println(Box1 + Box2);
        //Вычитание
        System.out.println(Box3 - Box4);
        //Умножение
        System.out.println(Box5 * Box6);
        //Деление
        System.out.println(Box7 / Box8);
    }
}